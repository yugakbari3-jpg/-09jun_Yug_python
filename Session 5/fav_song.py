user = input("Enter yor favorite song;")

for char in user:
    if char == 'a':
        print('a')
    elif char == 'e':
        print('e')
    elif char == 'i':
        print('i')
    elif char == 'o':
        print('o')
    elif char == 'u':
        print('u')
    else:
        print("Error occured: No vowels found")
    
      

  